//Bean Opinions

public class Opinions {
    private String opicodi;
    private String observacio;
    private String puntuacio;
    private String opirevisada;

    public String getOpicodi() {
        return opicodi;
    }

    public void setOpicodi(String opicodi) {
        this.opicodi = opicodi;
    }

    public String getObservacio() {
        return observacio;
    }

    public void setObservacio(String observacio) {
        this.observacio = observacio;
    }

    public String getPuntuacio() {
        return puntuacio;
    }

    public void setPuntuacio(String puntuacio) {
        this.puntuacio = puntuacio;
    }

    public String getOpirevisada() {
        return opirevisada;
    }

    public void setOpirevisada(String opirevisada) {
        this.opirevisada = opirevisada;
    }
}
